package Logic;

public class user {

	String name;
	int score;
	double time;

	public user() {
	
	}


public String getName() {
	return name;
}

public void setName(String name) {
	this.name = name;
}

public int getScore() {
	return score;
}

public void setScore(int score) {
	this.score = score;
}


public double getTime() {
	return time;
}

public void setTime(double time) {
	this.time = time;
}

}